                        <h1><span style="font-weight: 600">Rocket to Makaha</span></h1>
                        <h2><span style="font-weight: 600">Time Period: 1950's</span></h2>
                        <img class="timeline-feature" src="images/1950s/makaha_1953_dick_metz.jpg" id="img2" alt="" />
