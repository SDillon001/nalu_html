                        <h1><span style="font-weight: 600">The Overwhelming North Shore</span></h1>
                        <h2><span style="font-weight: 600">Time Period: 1940's</span></h2>
                        <img class="timeline-feature" src="images/1940s/the_overwhelming_north_shore_2_bolster.jpg" id="img2" alt="" >
