                            <p>It was a given that Hawaii, uniquely positioned as it was to the North Pacific storm track—close enough to bring in the power; distant enough that swells had time to groom themselves—was the only place in the world able to produce huge, clean, rideable waves. This still isn’t wrong. No other coastal region can match Hawaii for big-wave consistency, and only here are big-wave breaks found in clusters. The North Shore alone is home to at least six reefs capable of producing twenty-five-foot waves. Still, Todos Santos and Pico Alto expanded big-wave surfing’s horizons. There were other big waves out there, everybody suddenly realized. Maybe lots of them.</p>

