                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>The seven-mile North Shore wave zone that began at Haleiwa was denser and more complex than any other like-sized area in the world, with breaks often shingled one on top of the other. It was overwhelming, particularly in the early years. A handful of locals surfers rode Haleiwa in the 1920s and early 1930s, but only the small nearshore waves. When they paddled out on a bigger day in 1938, Whitey Harrison and Tarzan Smith were rewarded with nothing more than a good long watery beating.</p>

                        <p>The North Shore was about the same distance from Waikiki as Makaha, but much easier to get to: drive straight across the island on the well-paved Kamehameha Highway, through the cane fields and past the tent rows at Schofield Barracks, then bear right, about one mile inland, just past the big gray smokestack at the Waialua Sugar Mill. </p>

                        <blockquote class="equip-merrick">Sunset Beach quickly revealed itself as the North Shore’s most consistent break. Like Makaha, Sunset is a right-breaking wave with a variety of component parts. Unlike Makaha, it takes shape as a wedge, not a wall; it has alternating steep and flat sections and a takeoff area that ranges fifty yards in any direction from wave to wave. Makaha Point Surf breaks with force equal to that of Sunset. But Sunset is a more complicated spot, and harder to ride.</blockquote>

                        <p>And so it went for the Empty Lot Boys and their hot curl converts, into the fall of 1941. They lived at a nearly surreal remove from the rest of the world, both geographically and psychologically. Jobs were still hard to come by in Honolulu. The war in Europe raged on. Japan was carving up Southeast Asia. None of that seemed to matter. The winter wave season had just arrived, and Hawaii’s surfing vanguard spent the first three weeks in December riding Makaha, or making plans to do so, and talking a lot about who had a faster board, Froiseth or Brown. These were the important things.</p>
