                            <h1>The Surf Riders of Hawaii</h1>
                            <img src="images/pre-1900s/pre_1900s_gurrey_surf_riders.jpg" id="img3" alt="" /></a>
                          </div>

                          <div class="col-2">
                            <div class="description"></div>
                            <p>Not much is known about Gurrey's early life. He was born (1874) in Kansas, and took an engineering degree from the University of California, Berkeley, before moving with his family to Hawaii, probably around 1899. Shortly after he married Caroline Haskins, a nationally known portrait photographer, and the two operated a series of galleries in Honolulu for the next two decades.</p>

                            <p>Gurrey took to surfing shortly after arriving in Honolulu, and became a member of the Hui Nalu surf club, where he shared waves with Duke Kahanmoku, and began to photograph his fellow surfers. Most of Gurrey's shots are taken from the side; taken from a pier, or from a canoe riding alonside his subjects. His work first appeared in surfing promoter Alexander Hume Ford's publication Mid-Pacific Magazine, in 1911. A few years later Gurrey collected the best of his photographs and included them in The Surf Riders of Hawaii. The booklet is roughly 7" x 7", contains eight board-mounted gelatin-silver images, and a short block of Gurrey-penned text on surfing.</p>
                            <p class="timeline-credit"><a href="https://eos.surf/history/" target="_blank">Source: Warshaw, Matt. The History of Surfing</a></p>
