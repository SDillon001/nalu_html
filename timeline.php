                        <img class="timeline-feature" src="images/1940s/malibu_the_magic_wave_adam_king.jpg" id="img2" alt="" />
                      </div>
                    </div>

                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>Why did Southern California give birth to a national surfing craze? One simple, attractive, ahistorical, quasi-magical explanation is that modern surfing conjured itself into existence from the perfect air that hung over the perfect beach next to the perfect waves at Malibu. Surfing actually made Malibu, but never mind. From the late 1940s to the early 1960s, it often seemed to be the other way around.</p>

                        <p>Just like at Waikiki and San Onofre, Malibu changed the sport on the land as well as in the water. This sandy hook of beachfront would in fact be surf culture’s last and greatest site-specific incubator. Loose-fitting clothes, bouncy slang, pride in the sport’s detachment from other sports (and the insolence that comes with that pride), a default wariness toward other surfers, flashing public nudity and other forms of mild social rebellion—a good part of the surfer stylebook was drafted at Malibu, beginning in 1945.</p>

                        <blockquote>No surf break, then or now, has ever presented itself as well as Malibu. Each wave rolled forth as if it had traveled halfway across the Pacific for the express purpose of gliding into this quarter-mile bit of shoreline.</blockquote>

                        <p>In a complete turnaround from the 1930s, surfers no longer showed any interest in organizing the sport through clubs or competitions. At this point, surfing had been part of Southern California’s recreational landscape for forty years. It had its own history, and was beginning to take on the heft of permanence, which likely helped the Malibu surfers make a bigger commitment to the sport. Lounging on the beach was a harder act to justify during the postwar prosperity boom than it had been during the Depression. In 1937, it was possible to ride waves for hours a day because there wasn’t much else to do. In 1947, serious water time was paid for with reduced paychecks and a much-shortened list of job choices. For a lot of surfers, this was a fair trade. They waited tables, worked as lifeguards, fished, or played out their GI Bill stipend...anything to stay on the beach.</p>
                        <p class="timeline-credit"><a href="https://eos.surf/history/" target="_blank">Source: Warshaw, Matt. The History of Surfing</a></p>
