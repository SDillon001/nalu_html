                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>A 1957 Look magazine cover story on American teenagers promised to explain “why they go wild, why they don’t listen.” The latest popular entertainment certainly offered no relief: Jerry Lee Lewis onanisticly pounding away at his piano was just fractionally less shocking than Little Richard with his black-on-black eyeliner and screeched vocals; B-movies like Girl Gang, Dragstrip Riot, and Rumble on the Docks were cheap to make and bulletproof at the box-office.</p>

                        <p>Although surfing was one Cosmo’s body-beautifying outdoor activities, surfers were more likely to be grouped with bikers and hot-rodders than junior division skiers and tennis players. That was fine with surfers. Most Mainland wave-riders played the rebel to one degree or another, starting with hairstyle. The “surfer look” meant longer hair—all the way to the collar, in some cases—and to shortcut the natural sun-lightening process, the new style was to treat it with peroxide, ammonia, or bleach, leaving the product in until every last strand was blowtorch-white. At the local Salvation Army, surfers pawed through the women’s formal-wear racks in search of long fur coats, to be worn robe-like over surf trunks at the beach.</p>

                        <blockquote class="equip-merrick">“Things had changed from the era of the Palos Verdes Surf Club,” Greg Noll later summarized, “when the guys were polite and behaved like gentleman, to something else where we just wanted to raise hell. And it wasn’t a gradual change. It was like someone threw a switch, and all of a sudden guys didn’t give a shit about society, or what other people thought of them.”</blockquote>

                        <p>Still, while surfing bred a certain amount of genuine rebelliousness, mostly it produced a lot of rebel posing. Even with their bleached-blond hair, moldy fur coats, and spray-painted rattletrap cars, America’s teenage surfers as a rule remained contented sons of suburbia. On hot summer days they lobbed water balloons at their girlfriends and had doughnut-eating contests on the beach. They didn’t carry weapons. They didn’t drop out at a rate any higher than that of the high school population at large. They didn’t thrill ride themselves to a gory death on the streets and highways.</p>
