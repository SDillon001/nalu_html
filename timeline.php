                        <p>At first, his contest results suffered. Richards finished third in 1976 final ratings, dropped to fifth the next year, then fell all the way to tenth in 1978. It wasn’t just that he was learning a trade. He was learning a trade while competing on the tour. In addition, too many contests were held in small waves, and as one of the tour’s biggest surfers, Richards found himself bogging down more often than the smaller, lighter pros.</p>

                        <p>Hoping to put a bit of snap and fizz into his small-wave act, Richards, alone among the top Aussie pros, made the switch in 1975 to the “stinger” design, introduced a year earlier by Ben Aipa of Hawaii.</p>

