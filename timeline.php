                        <p>Before the IPS world tour, the top shortboard-era surfers—Nat Young, Midget Farrelly, Gerry Lopez, Reno Abellira, and Terry Fitzgerald among them—earned a living by shaping surfboards. Once the tour was established, aspiring pros had more incentive to focus entirely on wave-riding, and boardmaking fell by the wayside.</p>

