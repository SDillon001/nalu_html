                        <h1><span style="font-weight: 600">The Rebel Next Door</span></h1>
                        <h2><span style="font-weight: 600">Time Period: 1950's</span> || Year: 1957</h2>
                        <img class="timeline-feature" src="images/1950s/rebel_next_door_modal.jpg" id="img2" alt="" />
