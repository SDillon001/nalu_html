                        <h1><span style="font-weight: 600">Hobie vs Velzy</span></h1>
                        <h2><span style="font-weight: 600">Time Period: 1940's</span> || Year: 1949</h2>
                        <img class="timeline-feature" src="images/1940s/hobie_vs_velzy_modal.jpg" id="img2" alt="" />
