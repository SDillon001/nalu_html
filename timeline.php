                      <div class="description timeline">
                        <p>Filmed in 1963 at the very height of the boom, <em>Endless Summer</em> wasn’t released to general audiences until fall 1966, at which point the movie played like a coda for a era of the sport that, rather than being endless, vanished almost instantly.</p>

                        <p>Brown’s premise was simple—follow two California surfers around the world in search of the perfect wave—and the end result had a casualness that almost seemed offhand. Brown picked his two surfers well. Robert August was Endless Summer’s calm, dark-haired goofyfooter. Mike Hynson was the grinning regularfooter with the permanently slicked-back blond hair.</p>

                        <p>Brown, August, and Hynson traveled light carrying one board each; Brown’s entire arsenal of camera gear, cases included, weighed less than a hundred pounds. There was no film crew. From California, over a four-month period, they circled the globe from west to east. They rode half-mile long waves in New Zealand, virgin reef surf in Senegal, and backwash swells in Tahiti. Hitting the most spectacular gusher in surf travel history, they also found what they'd gone looking for—the perfect wave—at a place called Cape St. Francis in South Africa.</p>

                        <p>Brown added the voice-over script during <em>Endless Summer’s</em> debut tour in 1964, when he screened the movie himself and did live narration. Technically the movie at that point was still a work in progress, and Brown often made small edits from one showing to the next, but the basic sequences were finished. So was all the subcontracted production work. A San Clemente band named the Sandals recorded the soundtrack, which included “Theme from Endless Summer”.</p>

                        <blockquote class="equip-merrick">Endless Summer presented real surfers traveling to real locations, and it couldn’t be described as anything other than a documentary. Except Brown didn’t really care about documenting his four-month trip around the world. What Brown wanted to do — what he did flawlessly — was present the look and feel of surfing in its best moments. In a way, Endless Summer was an impressionist work, and Brown was craftsman enough to arrange, conceal, accent, and outright invent as required.</blockquote>
