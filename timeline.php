                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>In April 1981, just a few weeks before the Dewey Weber Longboard Classic, an oversized Aussie pro surfer and boardmaker named Simon Anderson walked down the stairs at Bells Beach holding a new type of board he’d winkingly called the “Thruster.” It was a peculiar-looking craft, with a narrow nose and three small fins placed in a triangular cluster.</p>

                        <p>To a casual observer, there was no reason to think the Thruster was any different, or more viable, than the hundred or so half-cocked ideas had flared briefly over the past fifteen years before winding up on the board design scrapheap. But Anderson was about to prove that this was something very different indeed.</p>

                        <blockquote class="equip-merrick">The tri-fin would in fact became the biggest surfboard design breakthrough since the shortboard revolution.</blockquote>

                        <p>The most distinctive feature of the new board was a stabilizing third fin, placed behind the side fins, along the stringer, just a couple of inches from the back end. Three-finned boards had been tried before: Dick Brewer and Reno Abellira of Hawaii had designed a version in 1970 that featured a standard-sized middle fin flanked by a pair of small half-moon “finlets.”</p>

                        <p>What Anderson did, in other words, was pull a tail design halfway out of thin air and patch it together with another shaper’s failed bit of nose design. Anderson called his new board the “Thruster.” He claimed it wasn’t a sexual double entendre. Nobody believed him.</p>

                        <p>“Bells was the real test,” Anderson said later. “We hadn’t really evolved yet to a place where you could start a bottom turn, aim up, and actually increase speed through your turn off the top. At Bells, all of a sudden, there it was.”<p>

                          <p>By the summer of 1982, the Thruster was being copied by every forward-thinking boardmaker in the sport and by 1983 it was being used almost universally on shortboards and was popular as well among longboarders. There was plenty of fine-tuning to come, but as of 2015 the tri-fin remains the standard board design.</p>
