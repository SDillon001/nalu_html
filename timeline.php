                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>In the early 1950s, a handful of Honolulu surfers decided to tackle big-wave riding as a kind of Manhattan Project. John Kelly and the other original hot curlers had first ridden Makaha in 1937, and over the next fifteen years ambitious Hawaiians had continued making occasional day-trip visits to test themselves in incrementally bigger waves. They hadn’t made much progress, though, and the Makaha trip was always a risk.</p>

                        <p>Downing made a board he called the Rocket—a 10-foot, 35-pound, balsa-core beauty now regarded as the first great piece of specialized big-wave equipment. Made for the upcoming 1950-51 winter surf season at Makaha, the Rocket was essentially a hot curl crossed with a Malibu chip. Downing kept the hot curl’s streamlined shape, but flattened out the back hull, knowing that a non-rounded planing surface would run faster through the water.</p>

                        <blockquote class="equip-merrick">The board was fiberglassed, like the chip boards, and had a strange 10-inch redwood-lined slot routered into the bottom, near the tail. Downing had changed his mind about the fin, and was now convinced that it would be the key stabilizing feature in big surf, but he wanted to try a few different sizes and shapes—this “fin box” would allow him to experiment.</blockquote>

                        <p>It took Downing a few weeks (and a few cartwheeling Point Surf wipeouts) to come up with a fin solution, but from then on it was a big-wave surfer-surfboard partnership like no other. Downing rode the Rocket for ten years. “I had so much confidence in this board,” he later said, “that never once, if I got it trimming right, did I feel like I couldn’t make it to the end of the curl line.” In 2010, sixty years after launch, the Rocket still had a place in Downing’s board collection.</p>
