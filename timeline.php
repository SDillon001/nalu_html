                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>It may seem a bit grandiose that anything having to do with surfing should be called a “revolution,” but the late-'60s shortboard revolution was just that—in miniature, at least. Old ways and ideas were thrown over in a furious rush.</p>

                        <p>Surfboard design was the most obvious change. Boards went from bulky 9-foot 6-inch 26-pound noseriders to 6-foot 6-inch 9-pound “mind machines.” Shortboards had numerous practical advantages over what were soon called (and always disparagingly) “longboards.” They were easy to carry, fit anywhere in the house, and could be stowed inside a car—smaller versions even fit in the trunk.</p>

                        <blockquote class="equip-merrick">The shortboard revolution thundered across the surfing landscape for roughly three years, beginning in 1967. It was really two distinct but conjoined movements: one technical, the other cultural.</blockquote>

                        <p>Shortboards changed the game. Exploring those changes, finding and testing and resetting performance limits—this was an all-consuming mission of surfing in the late '60s. Hanging ten was out. Straight-line trim was out. Now it was all about fin-flexing bottom turns, “roller coaster” moves off the whitewater, and tucking yourself into the “green room”—otherwise known as the tube.</p>

                        <p>Equipment and performance, though, were just the leading edge of the shortboard revolution, which reshaped every last nook and cranny of the surf-world landscape, from fashion and language, to media and competition, to ritual and doctrine. Surfing during this period did its best to abdicate from the world of sport. Surfers practically melted into the counterculture, in ways both engaging and absurd. </p>

                        <p>But the period also brought a new hardness to beaches and lineups. A territorial practice called “localism” became an abiding surf-world presence. The sport's first drug fatalities were counted. Surfing’s ingrained cooler-than-you pettiness clocked up, and was worse for all the canned peace-and-brotherhood rhetoric.</p>

                        <p>Lines between Us and Them were sharply drawn. A lot of knives were hidden behind the peace banners. All to be expected, however. Revolution, as Chairman Mao said, is not a dinner party.</p>
