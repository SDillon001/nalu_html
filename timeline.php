                        <h1><span style="font-weight: 600">Revolution is not a Dinner Party</span></h1>
                        <h2><span style="font-weight: 600">Time Period: 1960's</span> || Year: 1967</h2>
                        <img src="images/1960s/gerry_lopez_pipe_jeff_divine.jpg" id="img2" alt="" />
