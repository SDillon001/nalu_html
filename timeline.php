                        <p>Surf club membership wasn’t mandatory for California surfers, but by the late 1930s you could have driven a Packard Super 8 from San Diego to Santa Cruz and had a hard time loading it up with nonaffiliated surfers. California’s best-known and best-organized group was the Palos Verdes Surf Club, founded in 1935 by a surfing dentist named John “Doc” Ball. Just under thirty surfers took the club oath and became PVSC members during the 1930s. A dozen other similar clubs were formed up and down the coast, every one filled with young gung-ho middle-class surfers, roughly half of them college students.</p>

                        <p>With clubs on the rise, surfing took another inevitable step toward organized competition, which meant rules and standings, winners and losers, trophies and championships—the staff of life to sportsmen of all kind.</p>

                        <blockquote class="equip-merrick">Also, there was a flattery inherent in the rise of club-sponsored events, contests, and newsletters, in that they all promised to put the surfer at the center of the action. In a social and cultural sense, this was essential to the development of the sport. But it didn’t reflect the surfer’s experience in the water, which constantly veered into the wild, to places that had nothing to do with any regulated or organized form of sport.</blockquote>
