                        <p>Although surfing was one Cosmo’s body-beautifying outdoor activities, surfers were more likely to be grouped with bikers and hot-rodders than junior division skiers and tennis players. That was fine with surfers. Most Mainland wave-riders played the rebel to one degree or another, starting with hairstyle. The “surfer look” meant longer hair—all the way to the collar, in some cases—and to shortcut the natural sun-lightening process, the new style was to treat it with peroxide, ammonia, or bleach, leaving the product in until every last strand was blowtorch-white. At the local Salvation Army, surfers pawed through the women’s formal-wear racks in search of long fur coats, to be worn robe-like over surf trunks at the beach.</p>

