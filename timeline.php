                    <div class="col-sm-6 timeline">
                      <div class="description timeline">
                        <p>Not only was John Severson the go-to guy for surf logos and trademarks in the early '60s, he worked for free. Not because he was young and exploitable, but because he desperately needed to fill ad space for "The Surfer" — the rough little 36-page booklet that started off as a promo piece for his 1960 movie Surf Fever, and was later grandfathered in as the debut issue of Surfer magazine.</p>

                        <p>The Surfer wound up looking like a scruffy but earnest art school project, beginning with its horizontal format, grainy cover shot, and hand-lettered logotype. Doodled surf figures glide around the margins. Captions are often set vertically. Lots of real estate on any give page is left unprinted and white. Severson had always liked Doc Ball’s 1946 book California Surfriders, and he’d intended to make The Surfer a similar all-photo project.</p>

                        <blockquote class="equip-merrick">The Surfer went to press just before Easter in 1960. Still not quite sure if he’d created a magazine, a promo piece, or a book—the cover was initially going to be hardbound, but a cardboard stock was switched in at the last minute—Severson in the end ran a contents page subtitle describing The Surfer as his “First Annual Surf Photo Book.” His idea was that he’d follow up with a second edition in 1961 to go with next year’s surf film.</blockquote>

                        <p>Severson printed ten thousand copies of The Surfer at a total cost of $3,000; the per-unit wholesale cost was $1.00, and each issue retailed for $2.00—pricey for something not too far removed from a vanity project. When the 1960s surf boom arrived, Severson was the primary media gatekeeper between trade interests and surfers at large, and he went a long way toward making the transition less crass, if not less abrupt. </p>
